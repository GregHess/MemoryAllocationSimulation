
public class Partition {

	private int size = 0;
	private Job job = null;
	
	public Partition(int size){
		this.size = size;
	}
	
	public void setSize(int size){
		this.size = size;
	}
	
	public int getSize(){
		return this.size;
	}
	
	public void setJob(Job job){
		this.job = job;
	}
	
	public Job getJob(){
		return this.job;
	}
	
	/* hasJob()
	 * 
	 * Will return false if the partitions job variable is set.
	 * Will return false if the partitions job variable is null.
	 * 
	 */
	public boolean hasJob(){
		
		if(this.job == null){
			return false;
		}
		
		return true;
	}
}
